var tab_num;
var once_doc_show_num = 9;
var article_num = UStarZoneArticles.length;
// in order to create all buttons on the tab view
function InitAllTab()
{
    tab_num = parseInt(article_num / once_doc_show_num) + 1;
    ShowTabView(0);
    var tab_view = document.getElementById("tab_view");
    for (var i = 0; i != tab_num; ++i)
    {
        var tab_btn = document.createElement("span");
        tab_btn.className = "theme_look rough";
        tab_btn.innerText = i + 1;
        tab_btn.setAttribute("onclick", "ShowTabView(" + i + ")");
        tab_view.appendChild(tab_btn);
    }
}

// to show the tab
function ShowTabView(tab_num) 
{
    var doc_view = document.getElementById("doc_view");
    if (!doc_view) {
        return;
    }
    doc_view.innerHTML = "";
    var index = tab_num * once_doc_show_num;
    for (var i = 0; i != once_doc_show_num; ++i)
    {
        if (index == article_num)
        {
            return;
        }
        AppendContent(doc_view, UStarZoneArticles[index]);
        ++index;
    }
}

// to append tht content of the doc_view
function AppendContent(doc_view, info_ref)
{
    var item_date = document.createElement("span")
    var item_card = document.createElement("div");
    var item_article_info = document.createElement("div");
    var item_bottom_info = document.createElement("div");
    var item_writer = document.createElement("b");
    var item_title = document.createElement("a");
    var item_intro = document.createElement("p");
    var item_avator = document.createElement("img");
    item_title.innerText = info_ref.title;
    item_intro.innerText = info_ref.intro;
    item_writer.innerText = info_ref.writer.username;
    item_date.innerText = info_ref.date;
    item_title.href = info_ref.link;
    item_title.target = "_blank";
    item_avator.src = info_ref.writer.avator;
    item_card.className = "item_card rough";
    item_article_info.className = "article_info";
    item_bottom_info.className = "bottom_info";

    item_article_info.appendChild(item_title);
    item_article_info.appendChild(item_intro);
    item_bottom_info.appendChild(item_avator);
    item_bottom_info.appendChild(item_writer);
    item_bottom_info.appendChild(item_date);
    item_card.appendChild(item_article_info);
    item_card.appendChild(item_bottom_info);
    doc_view.appendChild(item_card);
}
